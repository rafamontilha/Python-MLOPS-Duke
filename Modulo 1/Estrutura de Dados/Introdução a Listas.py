#listas são containers de itens
[1, 2, 3, 4, 5]

#listas podem contar itens de tipos diferentes
[1, "dois", 3.0, [4, 5]]
#você pode encontrar o comprimento de uma lista usando a função len()
len([1, 2, 3, 4, 5]) #5

#cada item em uma lista tem um índice, começando do 0
nums = [1, 2, 3]

print(nums[0]) #1
#devemos usar colchetes para acessar um item em uma lista, usando seu índice
print(nums[1]) #2

#você pode usar índices negativos para acessar itens a partir do final da lista
print(nums[-1]) #3

